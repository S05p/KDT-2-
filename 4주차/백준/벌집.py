# 1         1개     0       > 1개   > 0     > -1
# 2~7       6개     1~6     > 2개   > 0~1   > 0
# 8~19      12개    7~18    > 3개   > 1~3   > 1~2
# 20~37     18개    19~36   > 4개   > 3~6   > 3~5
# 38~61     24개    37~60   > 5개   > 6~10  > 6~9

N = int(input())

n = -1

while True:
    n += 1
    if N == 1:
        print(1)
        break
    elif (n*(n+1))/2 > (N-2)/6:
        print(n+1)
        break