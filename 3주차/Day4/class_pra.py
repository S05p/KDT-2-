class calculator:
    
    def plus(number1, number2):
        return number1 + number2
    
    def minus(number1, number2):
        return number1 - number2
    
    def multiply(number1, number2):
        return number1*number2
    
    def division(number1, number2):
        return number1//number2
    
    
print(calculator().plus(10,5))